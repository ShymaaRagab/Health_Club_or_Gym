    private void coach_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_coach_searchActionPerformed
        // TODO add your handling code here:

        if(coach_id.getText().isEmpty()){
            Masseges.showErrorDialog(AllClasses.Masseges.errorEmptyField);
        }else{
            try{
                SearchCoach search = new SearchCoach();
                ArrayList<String> searchCoach = search.searchCoach(Integer.parseInt(coach_id.getText()));
                if(searchCoach.isEmpty()){
                    System.out.println(searchCoach);
                    Masseges.showErrorDialog("User Doesn't Exist");
                }else{
                    int searchCounter = 0;
                    String[] columnsName = {"ID", "Name","Age"};
                    String[][] data = new String [(searchCoach.size())/3][3];
                    for (int i = 0 ; i<data.length ; i++){
                        for(int j=0 ; j<3 ; j++){
                            data[i][j] = searchCoach.get(searchCounter);
                            searchCounter++;
                        }
                    }
                    DefaultTableModel model = new DefaultTableModel(data, columnsName);
                    table.setModel(model);
                    table.setDefaultEditor(Object.class, null);
                }
            }catch (SQLException ex) {
                Logger.getLogger(Admin_List_Coach.class.getName()).log(Level.SEVERE, null, ex);
                Masseges.showErrorDialog(AllClasses.Masseges.errorUpdatingDatabase);
            }
        }

    }//GEN-LAST:event_coach_searchActionPerformed

