    private void CoachDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CoachDeleteActionPerformed

        if(coach_id.getText().isEmpty()){
            Masseges.showErrorDialog(AllClasses.Masseges.errorEmptyField);
        }else{
            try{
                Dlete del = new Dlete();
                del.delete(Integer.parseInt(coach_id.getText()),"COACH");
                Masseges.showInformationDialog("Deleted Successfully");
            }catch (SQLException ex) {
                Logger.getLogger(Admin_List_Coach.class.getName()).log(Level.SEVERE, null, ex);
                Masseges.showErrorDialog(AllClasses.Masseges.errorUpdatingDatabase);
            }
        }
    }//GEN-LAST:event_CoachDeleteActionPerformed

