    private void update_passwordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_passwordActionPerformed
        // TODO add your handling code here:

        if(member_old_password.getText().isEmpty() || member_new_password.getText().isEmpty() || member_con_new_password.getText().isEmpty()){
            Masseges.showErrorDialog(Masseges.errorEmptyField);
        }else{
            try{
                if(member_old_password.getText().equals(AllClasses.adminTasks.getInstance().oldPass(this.id,"MEMBER"))){
                    System.out.println("hi");
                    if(member_new_password.getText().equals(member_con_new_password.getText())){
                        Update updateMember = new Update();
                        updateMember.update(this.id,member_new_password.getText(),"PASSWORD","MEMBER");
                        Masseges.showInformationDialog("Update Succesfully");
                    }else{
                        Masseges.showErrorDialog("Password don't Match");
                    }

                }else{
                    Masseges.showErrorDialog("Wrong Old Password");
                }
            }catch (SQLException ex) {
                Logger.getLogger(Admin_Update_Memeber.class.getName()).log(Level.SEVERE, null, ex);
                Masseges.showErrorDialog(Masseges.errorUpdatingDatabase);
            }

        }
    }//GEN-LAST:event_update_passwordActionPerformed

    private void show_infoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show_infoActionPerformed
        // TODO add your handling code here:

        try{
            SearchMember search = new SearchMember();
            ArrayList<String> member = search.searchMember(this.id);
            int counter = 0;
            String[] columnsName = {"ID", "Name","Age","Assigned Coach"};
            String[][] data = new String [(member.size())/4][4];
            for (int i = 0 ; i<data.length ; i++){
                for(int j=0 ; j<4 ; j++){
                    data[i][j] = member.get(counter);
                    counter++;
                }
            }
            DefaultTableModel model = new DefaultTableModel(data, columnsName);
            table.setModel(model);
            table.setDefaultEditor(Object.class, null);
        } catch (SQLException ex) {
            Logger.getLogger(Admin_List_Coach.class.getName()).log(Level.SEVERE, null, ex);
            Masseges.showErrorDialog(Masseges.errorUpdatingDatabase);
        }

    }//GEN-LAST:event_show_infoActionPerformed

