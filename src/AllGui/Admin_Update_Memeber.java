    private void update_nameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_nameActionPerformed
        // TODO add your handling code here:

        if(member_name.getText().isEmpty()){
            Masseges.showErrorDialog(AllClasses.Masseges.errorEmptyField);
        }else{
            try{
                Update updateMember = new Update();
                updateMember.update(this.id,member_name.getText(),"NAME","MEMBER");
                Masseges.showInformationDialog("Update Succesfully");
            }catch (SQLException ex) {
                Masseges.showErrorDialog(Masseges.errorUpdatingDatabase);
            }
        }

    }//GEN-LAST:event_update_nameActionPerformed

